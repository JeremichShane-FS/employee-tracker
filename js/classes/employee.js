// Employee class is a blueprint for creating employee objects.  It has three properties: name, age, and annualSalary.  The constructor method is used to initialize the properties of the Employee class.
class Employee {
  constructor(name, age, annualSalary) {
    this.name = name;
    this.age = age;
    this.annualSalary = annualSalary;
  }
}

export default Employee;
